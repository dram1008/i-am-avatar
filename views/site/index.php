    <p>Теперь я — часть невидимого братства, которое глубоко любит человечество. Мы — ваши братья; мы слышим ваши молитвы, ваши медитации и наблюдаем ваши движения туда и сюда. Мы — это те, кто жил здесь как человек и испытал отчаяние, горе и радость, которые вам всем известны. Но мы познали, как преодолеть ограничения человеческого опыта и выйти за его пределы, чтобы осознать более возвышенное состояние бытия.</p>

