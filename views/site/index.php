    <p>Я здесь, чтобы напомнить вам о наследии, о котором большинство из вас давным-давно позабыли. Я пришёл предложить вам более возвышенное миропонимание, из которого вы бы сами сделали выводы и поняли, что вы действительно божественные и бессмертные создания, которые всегда были любимы и всегда поддерживались сущностью, имя которой Бог. Я здесь, чтобы помочь вам осознать, что только вы своим возвышенным разумом создали каждую реальность своей жизни и что той же самой силой вы имеете возможность сотворить и испытать любую реальность, какую только пожелаете.</p>

